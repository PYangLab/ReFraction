# utility functions
computeWeight <- function(x, aa.weight){
   aa.count <- table(factor(x, levels = levels(SEQINR.UTIL$CODON.AA$L)))
   sum(aa.weight * aa.count[2:21]) / 1000;
} 

countAA <- function(x){
   aa.count <- table(factor(x, levels = levels(SEQINR.UTIL$CODON.AA$L)))[c("D", "E", "C", "Y", "H", "K", "R")]
} 

len <- function(x){
   x[(nchar(x) >= 7)]
}


computeProteinPI <- function(keyAA, tab){ 

   AspNumber <- keyAA[1]
   GluNumber <- keyAA[2]
   CysNumber <- keyAA[3]
   TyrNumber <- keyAA[4]
   HisNumber <- keyAA[5]
   LysNumber <- keyAA[6]
   ArgNumber <- keyAA[7] 

   NQ <- 0.0; #net charge in given pH  
   QN1 <- 0;  #C-terminal charge
   QN2 <- 0;  #D charge
   QN3 <- 0;  #E charge
   QN4 <- 0;  #C charge
   QN5 <- 0;  #Y charge
   QP1 <- 0;  #H charge
   QP2 <- 0;  #NH2 charge
   QP3 <- 0;  #K charge
   QP4 <- 0;  #R charge
        
   pH <- 6.5;             #starting point pI <- 6.5 - theoretically it should be 7, but average protein pI is 6.5 so we increase the probability
   pHprev <- 0.0;         #of finding the solution
   pHnext <- 14.0;        #0-14 is possible pH range
   X <- 0.0;
   E <- 0.01;           #epsilon means precision [pI <- pH ? E]
   temp <- 0.0;
   tabl <- 650;

   while(TRUE){
      QN1 <- tab[tabl, 1];
      QN2 <- -AspNumber / tab[tabl, 2];
      QN3 <- -GluNumber / tab[tabl, 3];
      QN4 <- -CysNumber / tab[tabl, 4];
      QN5 <- -TyrNumber / tab[tabl, 5];
      QP1 <- HisNumber / tab[tabl, 6];
      QP2 <- tab[tabl, 7];
      QP3 <- LysNumber / tab[tabl, 8];
      QP4 <- ArgNumber / tab[tabl, 9];
 
	    NQ <- QN1 + QN2 + QN3 + QN4 + QN5 + QP1 + QP2 + QP3 + QP4;

      ######### BISECTION #########
	    if (NQ < 0) {
         temp <- pH;
         pH <- pH -((pH - pHprev) / 2);
         pHnext <- temp;
	    } else {
         temp <- pH;
         pH <- pH + ((pHnext - pH) / 2);
         pHprev <- temp;	
	    }

      if ((pH - pHprev < E) & ( pHnext - pH < E)){ #terminal condition, finding isoelectric point with given precision
         break;
	    }

      #%%%%%%%%%%%%%%%%%%%   cutting off pH to two decimals   %%%%%%%%%%%%%%%%%%%
      n <- pH * 100;
      obc <- n;     #//projection double to int
      ulamek <- n - obc;
	    if(ulamek >= 0.5) {
         obc <- obc + 1; 
	    }
      tabl <- obc;
      pH <- obc; #//after that we are move to double again        
      pH <- pH /100;
   }

   return(pH);
}
